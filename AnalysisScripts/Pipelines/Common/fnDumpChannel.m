function fnDumpChannel(strctAnalog, strOutFileName)
[strPath,strFile]=fileparts(strOutFileName);
if ~exist(strPath,'dir')
    mkdir(strPath)
end

clear strctHeader
Cnt = 0;
% Dump information to file....
hFileID = fopen(strOutFileName,'wb+');
iHeaderPrefix = fwrite(hFileID, 'KOFIKO_v1.00A','char'); % Identifier...
Cnt=Cnt+8*fwrite(hFileID,0 ,'uint64'); % HeaderSize

Cnt=Cnt+8*fwrite(hFileID,strctAnalog.m_iChannel ,'uint64'); % Identifier...
Cnt=Cnt+8*fwrite(hFileID,strctAnalog.m_fSamplingFreq ,'uint64'); % Identifier...
Cnt=Cnt+8*fwrite(hFileID,length(strctAnalog.m_strChannelName),'uint64'); % Identifier...
Cnt=Cnt+1*fwrite(hFileID,strctAnalog.m_strChannelName ,'char'); % Identifier...
Cnt=Cnt+8*fwrite(hFileID,length(strctAnalog.m_aiNumSamplesPerFrame),'uint64'); % Identifier...
Cnt=Cnt+8*fwrite(hFileID,strctAnalog.m_aiNumSamplesPerFrame ,'uint64'); % Identifier...
Cnt=Cnt+8*fwrite(hFileID,strctAnalog.m_afStartTS ,'double'); % Identifier...

iDataWritten = fwrite(hFileID,strctAnalog.m_afData ,'single'); % Identifier...

fseek(hFileID,iHeaderPrefix,'bof');
fwrite(hFileID,Cnt,'uint64');
fclose(hFileID);

return;
